BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS "Countries" (
	"name"	char(40),
	"country_id"	char(3) UNIQUE,
	"area_sqkm"	integer,
	"population"	integer,
	PRIMARY KEY("country_id")
);
CREATE TABLE IF NOT EXISTS "Players" (
	"name"	char(40),
	"player_id"	char(10) UNIQUE,
	"country_id"	char(3),
	PRIMARY KEY("player_id"),
	FOREIGN KEY("country_id") REFERENCES "Countries"("country_id")
);
CREATE TABLE IF NOT EXISTS "Olympics" (
	"olympic_id"	char(7) UNIQUE,
	"country_id"	char(3),
	"city"	char(50),
	"year"	integer,
	PRIMARY KEY("olympic_id"),
	FOREIGN KEY("country_id") REFERENCES "Countries"("country_id")
);
CREATE TABLE IF NOT EXISTS "Events" (
	"event_id"	char(7) UNIQUE,
	"name"	char(40),
	"eventtype"	char(20),
	"olympic_id"	char(7),
	"is_team_event"	integer CHECK("is_team_event" IN (0, 1)),
	"num_players_in_team"	integer,
	"result_noted_in"	char(100),
	PRIMARY KEY("event_id"),
	FOREIGN KEY("olympic_id") REFERENCES "Olympics"("olympic_id")
);
CREATE TABLE IF NOT EXISTS "Results" (
	"event_id"	char(7),
	"player_id"	char(10),
	"medal"	char(7),
	"result"	float,
	PRIMARY KEY("event_id","player_id"),
	FOREIGN KEY("event_id") REFERENCES "Events"("event_id"),
	FOREIGN KEY("player_id") REFERENCES "players"("player_id")
);
